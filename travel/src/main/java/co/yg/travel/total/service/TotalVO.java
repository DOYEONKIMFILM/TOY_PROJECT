package co.yg.travel.total.service;

public class TotalVO {
	
	private int totalNo;
	private int typePlan;
	private int typeNoplan;
	private int typeRespect;
	
	public int getTotalNo() {
		return totalNo;
	}
	public void setTotalNo(int totalNo) {
		this.totalNo = totalNo;
	}
	public int getTypePlan() {
		return typePlan;
	}
	public void setTypePlan(int typePlan) {
		this.typePlan = typePlan;
	}
	public int getTypeNoplan() {
		return typeNoplan;
	}
	public void setTypeNoplan(int typeNoplan) {
		this.typeNoplan = typeNoplan;
	}
	public int getTypeRespect() {
		return typeRespect;
	}
	public void setTypeRespect(int typeRespect) {
		this.typeRespect = typeRespect;
	}
	
}
